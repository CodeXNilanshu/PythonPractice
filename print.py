## Python adds space automatically, when we use comma and it is called seperator
print('Hello','Nilanshu')

## If we don't want default space then we can write sep='' and space will disappear
print("Hello", "Nilanshu", sep='')

## We can write down anything in the seperator and we can use double quote "" and single quote '' together like example below
print("Hello", "Nilanshu", 'Super Cool' , sep=' : : ')

## Now all the outputs are in the next line but if we want them in single line then we use end= it tells that we want the next output in the same line as this output, by default it goes to the next line but we don't want that.

## These end and seperators are specific to one print statement
print("Hello", "Nilanshu", end='')
print('Hello', 'Nilanshu2', end = ' -> ')
print('Bye', 'Nilanshu3')

